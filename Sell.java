    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Str = Search.getText();
        try {
            File f = new File("Pharmacy.txt");
            Scanner scan = new Scanner(f);
            int flag = 0;
            while (scan.hasNextLine()) {
                String a = scan.nextLine();
                String[] md = a.split(" ");
                Id = md[0];
                name = md[1];
                Cname = md[2];
                Quantity = Integer.parseInt(md[3]);
                rate = Integer.parseInt(md[4]);

                if (Id.compareTo(Str) == 0 || name.compareTo(Str) == 0) {
                    JOptionPane.showMessageDialog(null, "found");
                    ID.setText(Id);
                    M_name.setText(name);
                    C_name.setText(Cname);
                    Price.setText(md[4]);
                    Unit.setText(String.valueOf(Quantity));

                    flag++;
                    break;

                }

            }
            if (flag == 0) {
                JOptionPane.showMessageDialog(null, "Not found");
            }

        } catch (Exception e) {
            System.out.println("caught3");
        } // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

