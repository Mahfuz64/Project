    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String str = Search.getText();
        Dashboard D = new Dashboard();
        ArrayList<String> listOfStrings = new ArrayList<String>();
        try {

            BufferedReader bf = new BufferedReader(new FileReader("Pharmacy.txt"));
            String Line = bf.readLine();
            while (Line != null) {
                listOfStrings.add(Line);
                Line = bf.readLine();
            }
            bf.close();
            File f=new File("Pharmacy.txt");
            f.delete();
            
            String[] array = listOfStrings.toArray(new String[0]);
            int quanti = Integer.parseInt(quan.getText());
            int total = quanti * rate;
            Quantity=Quantity-quanti;
            
            D.balance(total);

            JOptionPane.showMessageDialog(null, "Sell Complete.Total:" + total);
            clearfields();
            for (int x = 0; x < array.length; x++) {
                String[] word = array[x].split(" ");

                if (word[0].compareTo(str) == 0 || word[1].compareTo(str) == 0) {
                    word[3] = String.valueOf(Quantity);
                    String join1 = String.join(" ", word[0], word[1], word[2], word[3], word[4]);
                    array[x] = join1;
                    break;
                }
            }
           
            FileWriter Medicine = new FileWriter("Pharmacy.txt",true);
            for (int x = 0; x < array.length; x++) {
                Medicine.write(array[x]);
               Medicine.write(System.getProperty("line.separator"));
                
            }
            Medicine.close();
        } catch (FileNotFoundException e) {

        }  catch (IOException e) {
            System.out.println(e);
        }
        dispose();
        D.show();

// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

